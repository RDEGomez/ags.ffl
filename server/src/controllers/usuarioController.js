exports.agregarJugadorAEquipo = async (req, res) => {
  const timestamp = new Date().toISOString();
  console.log(`\n‚öΩ [${timestamp}] INICIO - Agregar jugador a equipo`);
  console.log('üì® Body recibido:', JSON.stringify(req.body, null, 2));

  try {
    const { usuarioId, numero, equipoId } = req.body;
    const usuarioLogueado = req.usuario;

    console.log('üîç Validando par√°metros...');
    console.log(`  üë§ Usuario ID: ${usuarioId}`);
    console.log(`  üèà Equipo ID: ${equipoId}`);
    console.log(`  üî¢ N√∫mero: ${numero}`);

    console.log('üîê Validando permisos...');
    
    const puedeAgregar = usuarioLogueado.rol === 'admin' || 
                        usuarioLogueado.rol === 'capitan' || 
                        usuarioLogueado._id.toString() === usuarioId;

    if (!puedeAgregar) {
      console.log('‚ùå ERROR: Sin permisos para agregar jugador');
      return res.status(403).json({ 
        mensaje: 'No tienes permisos para agregar este jugador al equipo' 
      });
    }

    console.log('‚úÖ Permisos validados');
    console.log(`üë§ Usuario logueado: ${usuarioLogueado.nombre} (${usuarioLogueado.rol})`);

    console.log('üîç Buscando jugador...');
    const jugador = await Usuario.findById(usuarioId);
    if (!jugador) {
      console.log('‚ùå ERROR: Jugador no encontrado');
      return res.status(404).json({ mensaje: 'Jugador no encontrado' });
    }

    console.log('üîç Buscando equipo...');
    const equipo = await Equipo.findById(equipoId);
    if (!equipo) {
      console.log('‚ùå ERROR: Equipo no encontrado');
      return res.status(404).json({ mensaje: 'Equipo no encontrado' });
    }

    console.log('‚úÖ Jugador y equipo encontrados');
    console.log(`  üë§ Jugador: ${jugador.nombre} (${jugador.email})`);
    console.log(`  üèà Equipo: ${equipo.nombre} (${equipo.categoria})`);

    // Validar que el jugador puede estar en equipos
    if (!jugador.puedeEstarEnEquipos()) {
      console.log('‚ùå ERROR: El usuario no puede estar en equipos');
      return res.status(400).json({ 
        mensaje: 'Este usuario no puede formar parte de equipos' 
      });
    }

    // Validar reglas de la categor√≠a
    console.log('üîç Validando reglas de categor√≠a...');
    const puedeUnirse = reglasCategorias.puedeUnirseACategoria(jugador, equipo.categoria);
    if (!puedeUnirse.puede) {
      console.log('‚ùå ERROR: No cumple reglas de categor√≠a');
      return res.status(400).json({ mensaje: puedeUnirse.razon });
    }

    // Verificar si ya est√° en el equipo
    const yaEstaEnEquipo = jugador.equipos.some(eq => eq.equipo.toString() === equipoId);
    if (yaEstaEnEquipo) {
      console.log('‚ùå ERROR: Jugador ya est√° en el equipo');
      return res.status(400).json({ mensaje: 'El jugador ya est√° en este equipo' });
    }

    // Verificar si el n√∫mero ya est√° ocupado en el equipo
    if (numero) {
      const numeroOcupado = await Usuario.findOne({
        'equipos.equipo': equipoId,
        'equipos.numero': numero
      });
      
      if (numeroOcupado) {
        console.log('‚ùå ERROR: N√∫mero ya ocupado');
        return res.status(400).json({ 
          mensaje: `El n√∫mero ${numero} ya est√° ocupado en este equipo` 
        });
      }
    }

    console.log('üíæ Agregando jugador al equipo...');
    
    // Agregar al equipo
    jugador.equipos.push({
      equipo: equipoId,
      numero: numero || null
    });

    await jugador.save();

    console.log('‚úÖ Jugador agregado exitosamente');
    console.log('üì§ Enviando respuesta exitosa');
    console.log(`‚úÖ [${new Date().toISOString()}] FIN - Agregado exitoso\n`);

    res.json({ 
      mensaje: 'Jugador agregado al equipo exitosamente',
      jugador: {
        id: jugador._id,
        nombre: jugador.nombre,
        email: jugador.email,
        numero: numero
      },
      equipo: {
        id: equipo._id,
        nombre: equipo.nombre,
        categoria: equipo.categoria
      }
    });

  } catch (error) {
    console.log(`‚ùå [${new Date().toISOString()}] ERROR al agregar jugador:`);
    console.error('üí• Error completo:', error);
    console.log(`‚ùå [${new Date().toISOString()}] FIN - Agregar jugador fallido\n`);
    
    res.status(500).json({ 
      mensaje: 'Error al agregar jugador al equipo', 
      error: error.message 
    });
  }
};