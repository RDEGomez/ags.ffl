// üî• NUEVA FUNCI√ìN: REENVIAR VERIFICACI√ìN
exports.reenviarVerificacion = async (req, res) => {
  const timestamp = new Date().toISOString();
  console.log(`\nüîÑ [${timestamp}] INICIO - Reenviar verificaci√≥n`);
  console.log('üì® Body recibido:', JSON.stringify(req.body, null, 2));

  try {
    const { email } = req.body;

    if (!email) {
      return res.status(400).json({ mensaje: 'Email requerido' });
    }

    console.log('üîç Buscando usuario...');
    const usuario = await Usuario.findOne({ email });

    if (!usuario) {
      console.log('‚ùå ERROR: Usuario no encontrado');
      return res.status(404).json({ mensaje: 'Usuario no encontrado' });
    }

    if (usuario.emailVerificado) {
      console.log('‚ùå ERROR: Email ya verificado');
      return res.status(400).json({ mensaje: 'El email ya est√° verificado' });
    }

    // Verificar l√≠mite de intentos (m√°ximo 3 por hora)
    const horaAtras = new Date(Date.now() - 60 * 60 * 1000);
    if (usuario.intentosVerificacion >= 3 && usuario.updatedAt > horaAtras) {
      console.log('‚ùå ERROR: Demasiados intentos');
      return res.status(429).json({ 
        mensaje: 'Demasiados intentos de reenv√≠o. Intenta de nuevo en una hora.',
        intentosRestantes: 0
      });
    }

    console.log('üîë Generando nuevo token...');
    const tokenVerificacion = usuario.crearTokenVerificacion();
    
    // Resetear contador si ha pasado m√°s de una hora
    if (usuario.updatedAt <= horaAtras) {
      usuario.intentosVerificacion = 1;
    } else {
      usuario.intentosVerificacion += 1;
    }

    await usuario.save();

    console.log('üìß Reenviando email de verificaci√≥n...');
    const emailResult = await emailService.reenviarVerificacion(
      usuario.email, 
      tokenVerificacion,
      usuario.nombre
    );

    if (!emailResult.success) {
      console.log('‚ùå ERROR: No se pudo enviar el email');
      return res.status(500).json({ mensaje: 'Error al enviar email de verificaci√≥n' });
    }

    console.log('‚úÖ Email reenviado exitosamente');
    console.log(`‚úÖ [${new Date().toISOString()}] FIN - Reenv√≠o exitoso\n`);

    res.json({
      mensaje: 'Email de verificaci√≥n reenviado. Revisa tu bandeja de entrada.',
      intentosRestantes: Math.max(0, 3 - usuario.intentosVerificacion)
    });

  } catch (error) {
    console.log(`‚ùå [${new Date().toISOString()}] ERROR en reenv√≠o:`);
    console.error('üí• Error completo:', error);
    console.log(`‚ùå [${new Date().toISOString()}] FIN - Reenv√≠o fallido\n`);
    
    res.status(500).json({ 
      mensaje: 'Error al reenviar verificaci√≥n', 
      error: error.message 
    });
  }
};

