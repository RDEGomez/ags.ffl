    console.log('‚úÖ Token v√°lido, verificando usuario...');
    
    // Verificar cuenta
    usuario.emailVerificado = true;
    usuario.fechaUltimaVerificacion = new Date();
    usuario.tokenVerificacion = undefined;
    usuario.tokenVerificacionExpira = undefined;

    await usuario.save();

    console.log('‚úÖ Email verificado exitosamente');

    // üî• ENVIAR EMAIL DE BIENVENIDA
