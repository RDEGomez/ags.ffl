// üîê Generar token
const generarToken = (usuario) => {
  console.log('üîë Generando token para usuario:', usuario._id);
  return jwt.sign(
    {
      id: usuario._id,
      documento: usuario.documento,
      rol: usuario.rol
    },
    process.env.JWT_SECRET,
    {
      expiresIn: '1h'
    }
  );
};

